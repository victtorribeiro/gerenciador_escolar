    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        Usuario u = new Usuario();
        if (jTableUsuarios.getSelectedRow() != -1) {
            u.setId_usuario((int) jTableUsuarios.getValueAt(jTableUsuarios.getSelectedRow(), 0));
            u.setNome(jTableUsuarios.getValueAt(jTableUsuarios.getSelectedRow(), 1).toString());
            u.setUsuario(jTableUsuarios.getValueAt(jTableUsuarios.getSelectedRow(), 2).toString());

            Usuario_Diretor ud = new Usuario_Diretor(u);
            Principal_Diretor.jDesktopPane_Diretor.add(ud);
            ud.setVisible(true);
            ud.setPosition();
            jButtonNovo.setEnabled(false);
            jButtonAlterar.setEnabled(false);
            jButtonAtualizar.setEnabled(false);
            jButtonFechar.setEnabled(false);

        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed


