    public void readJTableUsuarios() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) jTableUsuarios.getModel();

        modelo.setNumRows(0);

        UsuarioDao ud = new UsuarioDao();
        for (Usuario u : ud.readUsuario()) {
            modelo.addRow(new Object[]{
                u.getId_usuario(),
                u.getNome(),
                u.getUsuario(),
                u.getCargo()
            });
        }
    }

