    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        turmas.setSegunda(Integer.parseInt(jComboBoxSegunda.getSelectedItem().toString()));
        turmas.setTerca(Integer.parseInt(jComboBoxTerca.getSelectedItem().toString()));
        turmas.setQuarta(Integer.parseInt(jComboBoxQuarta.getSelectedItem().toString()));
        turmas.setQuinta(Integer.parseInt(jComboBoxQuinta.getSelectedItem().toString()));
        turmas.setSexta(Integer.parseInt(jComboBoxSexta.getSelectedItem().toString()));

        TurmasDao td = new TurmasDao();
        if (turmas.getId_Turma() == 0) {

            try {
                td.cadastrarTurmas(turmas);

                dispose();
            } catch (SQLException ex) {
                Logger.getLogger(Turma_AulasSemanais.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            try {
                td.updateTurmas(turmas);
                
                dispose();
            } catch (SQLException ex) {
                Logger.getLogger(Turma_AulasSemanais.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }

        ListTurma_Diretor.jButtonNova.setEnabled(true);
        ListTurma_Diretor.jButtonAlterar.setEnabled(true);
        ListTurma_Diretor.jButtonAtualizar.setEnabled(true);
        ListTurma_Diretor.jButtonFechar.setEnabled(true);

    }//GEN-LAST:event_jButton1ActionPerformed


