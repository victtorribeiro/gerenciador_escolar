    public void readJTableMaterias() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) jTableMaterias.getModel();

        modelo.setNumRows(0);

        MateriaDao md = new MateriaDao();
        for (Materia m : md.readMateria()) {
            modelo.addRow(new Object[]{
                m.getId_materia(),
                m.getMateria()
            });
        }
    }
    
    

