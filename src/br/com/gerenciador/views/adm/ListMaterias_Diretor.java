    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        Materia m = new Materia();
        if (jTableMaterias.getSelectedRow() != -1) {
            m.setId_materia((int) jTableMaterias.getValueAt(jTableMaterias.getSelectedRow(), 0));
            m.setMateria(jTableMaterias.getValueAt(jTableMaterias.getSelectedRow(), 1).toString());

            Materia_Diretor md = new Materia_Diretor(m);
            Principal_Diretor.jDesktopPane_Diretor.add(md);
            md.setVisible(true);
            md.setPosition();
            jButtonNova.setEnabled(false);
            jButtonAlterar.setEnabled(false);
            jButtonAtualizar.setEnabled(false);
            jButtonFechar.setEnabled(false);

        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

