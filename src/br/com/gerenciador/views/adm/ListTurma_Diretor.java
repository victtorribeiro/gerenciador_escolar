    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        Turmas t = new Turmas();
        if (jTableTurmas.getSelectedRow() != -1) {
            t.setId_Turma((int) jTableTurmas.getValueAt(jTableTurmas.getSelectedRow(), 0));
            t.setSerie(jTableTurmas.getValueAt(jTableTurmas.getSelectedRow(), 1).toString());
            t.setTurma(jTableTurmas.getValueAt(jTableTurmas.getSelectedRow(), 2).toString());

            Turma_Diretor td = new Turma_Diretor(t);
            Principal_Diretor.jDesktopPane_Diretor.add(td);
            td.setVisible(true);
            td.setPosition();
            jButtonNova.setEnabled(false);
            jButtonAlterar.setEnabled(false);
            jButtonAtualizar.setEnabled(false);
            jButtonFechar.setEnabled(false);

        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

