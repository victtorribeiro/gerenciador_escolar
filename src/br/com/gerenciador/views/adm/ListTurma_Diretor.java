    private void jButtonAtualizar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizar1ActionPerformed
        try {
            Turmas t = new Turmas();
            t.setId_Turma((int) jTableTurmas.getValueAt(jTableTurmas.getSelectedRow(), 0));
            t.setTurma(jTableTurmas.getValueAt(jTableTurmas.getSelectedRow(), 1).toString() + " " + jTableTurmas.getValueAt(jTableTurmas.getSelectedRow(), 2).toString());
            TurmasDao td = new TurmasDao();
            Horario_AulasDao had = new Horario_AulasDao();
            
            if (had.verificaTurma(t.getTurma())) {
                JOptionPane.showMessageDialog(null, "Existe cadastros vinculados a turma. Exclua todos antes de exlcuir a turma!");
            } else {
                td.deleteTurmas(t.getId_Turma());
                JOptionPane.showMessageDialog(null, "Turma excluída com sucesso!");
                readJTableTurmas();
            }
        } catch (SQLException ex) {
            Logger.getLogger(ListTurma_Diretor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonAtualizar1ActionPerformed


