    public void readJTableTurmas() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) jTableTurmas.getModel();

        modelo.setNumRows(0);

        TurmasDao td = new TurmasDao();
        for (Turmas t : td.readTurmas()) {
            modelo.addRow(new Object[]{
                t.getId_Turma(),
                t.getSerie(),
                t.getTurma(),
                t.getTurno(),
                t.getData1()
            });
        }
    }

