    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Calendario c = new Calendario();
        if (jTableDatas.getSelectedRow() != -1) {
            if (jTableDatas.getValueAt(jTableDatas.getSelectedRow(), 3) == "Não") {
                JOptionPane.showMessageDialog(null, "Esta data não pode ser editada!");
            } else {

                c.setId_data((int) jTableDatas.getValueAt(jTableDatas.getSelectedRow(), 0));
                c.setDescricao(jTableDatas.getValueAt(jTableDatas.getSelectedRow(), 1).toString());
                c.setData(jTableDatas.getValueAt(jTableDatas.getSelectedRow(), 2).toString());

                Calendario_Academico ca = new Calendario_Academico(c);
                Principal_Diretor.jDesktopPane_Diretor.add(ca);
                ca.setVisible(true);
                ca.setPosition();
                jButton1.setEnabled(false);
                jButton3.setEnabled(false);
                jButton4.setEnabled(false);
                jButton4.setEnabled(false);

            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        Calendario c = new Calendario();
        CalendarioDao cDao = new CalendarioDao();
        c.setId_data((int) jTableDatas.getValueAt(jTableDatas.getSelectedRow(), 0));
        try {
            cDao.deleteData(c.getId_data());
            JOptionPane.showMessageDialog(null, "Evento excluído com sucesso!");
            readJTableDatas();
        } catch (SQLException ex) {
            Logger.getLogger(ListCalendarioAcademico.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton6ActionPerformed


