    public void readJTableDatas() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) jTableDatas.getModel();

        modelo.setNumRows(0);

        CalendarioDao cDao = new CalendarioDao();
        for (Calendario c : cDao.read()) {
            modelo.addRow(new Object[]{
                c.getId_data(),
                c.getDescricao(),
                c.getData(),
                c.getEditavelS()
            });
        }
    }

