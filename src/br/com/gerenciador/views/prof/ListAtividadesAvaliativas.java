    private void jButtonAlterarAtividadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarAtividadeActionPerformed
        Atividades_Avaliativas aa = new Atividades_Avaliativas();
        if (jTableAtividades.getSelectedRow() != -1) {
            aa.setId_atividade((int) jTableAtividades.getValueAt(jTableAtividades.getSelectedRow(), 0));
            aa.setObservacao(jTableAtividades.getValueAt(jTableAtividades.getSelectedRow(), 4).toString());
        }
        
        Atividade_Avaliativa aaV = new Atividade_Avaliativa(aa);
        
        Principal_Professor.jDesktopPane_Professor.add(aaV);
        aaV.setVisible(true);
        aaV.setPosition();
        jButtonAlterarAtividade.setEnabled(false);
        jButtonAtualizar.setEnabled(false);
        jButtonFechar.setEnabled(false);
        jButtonNovaAtividade.setEnabled(false);
    }//GEN-LAST:event_jButtonAlterarAtividadeActionPerformed

