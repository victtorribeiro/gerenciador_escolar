    private void jButtonAtualizar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizar1ActionPerformed
        Atividades_Avaliativas aa = new Atividades_Avaliativas();
        if (jTableAtividades.getSelectedRow() != -1) {
            aa.setId_atividade((int) jTableAtividades.getValueAt(jTableAtividades.getSelectedRow(), 0));
            Atividades_AvaliativasDao aad = new Atividades_AvaliativasDao();
            try {
                aad.deleteAtividade(aa.getId_atividade());
                JOptionPane.showMessageDialog(null, "Atividade excluída com sucesso!");
                readJTableAtividadas();
            } catch (SQLException ex) {
                Logger.getLogger(ListAtividadesAvaliativas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonAtualizar1ActionPerformed


