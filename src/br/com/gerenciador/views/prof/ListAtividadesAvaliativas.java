    public void readJTableAtividadas() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) jTableAtividades.getModel();

        modelo.setNumRows(0);

        Atividades_AvaliativasDao aad = new Atividades_AvaliativasDao();
        for (Atividades_Avaliativas aa : aad.readAtividades()) {
            modelo.addRow(new Object[]{
                aa.getId_atividade(),
                aa.getMateria(),
                aa.getTurma(),
                aa.getData(),
                aa.getObservacao()

            });
        }
    }

